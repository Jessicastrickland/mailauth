#pragma mark - Navigation

// In a storyboard-based application, you will often want to do a little preparation before navigation
- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if([sender isKindOfClass:[DeviceListCell class]])
    {
        MynigmaDevice* device = [(DeviceListCell*)sender device];
        if([segue.destinationViewController isKindOfClass:[SharedSecretTrustController class]])
        {
            [(SharedSecretTrustController*)segue.destinationViewController setupWithDevice:device];
        }
    }
}


