#import "DeviceListController.h"
#import "MynigmaDevice+Category.h"
#import "DeviceListCell.h"
#import "SharedSecretTrustController.h"


@interface DeviceListController ()

@end

@implementation DeviceListController

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    
    //make sure the current device is actually created
    [MynigmaDevice currentDevice];

    //list all devices
    NSArray* allDevices = [MynigmaDevice listAllKnownDevices];

    //remove all previous objects
    self.devices = [allDevices mutableCopy];

}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return self.devices.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    DeviceListCell *cell = (DeviceListCell*)[tableView dequeueReusableCellWithIdentifier:@"DeviceCell" forIndexPath:indexPath];

    //now add the devices
    MynigmaDevice* device = self.devices[indexPath.row];

    [cell setupWithDevice:device];
    
    return cell;
}


